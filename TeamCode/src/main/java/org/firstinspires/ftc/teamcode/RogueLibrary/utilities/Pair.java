package org.firstinspires.ftc.teamcode.RogueLibrary.utilities;


/**
 * Simple class implementing simple Pair functionality.
 * @param <Y> Class of Pair values;
 */
public class Pair<Y> {
    public final Y x;
    public final Y y;

    public Pair(Y x, Y y) {
        this.x = x;
        this.y = y;
    }
}
